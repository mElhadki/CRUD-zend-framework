<?php 
namespace Article\Form;

use Zend\Form\Form;

class CategoryForm extends Form
{
    public function __construct()
    {
        // we want to ignore the name passed
        parent::__construct('category');

        $this->add(array(
            'name' => 'id',
            'type' => 'Hidden',
        ));

        $this->add(array(
            'name' => 'name',
            'type' => 'Text',
        ));
        
        $this->add(array(
            'name' => 'submit',
            'type' => 'Submit',
            'attributes' => array(
                'value' => 'Go',
                'id' => 'submitbutton',
            ),
        ));
    }
}
?>